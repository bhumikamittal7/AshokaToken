let web3;
let account;
let quizData;
let currentQuestionIndex = 0;
let redeemedReward = false;
/*=======================================================================================================================================*/

async function connectWallet() {
  if (window.ethereum) {
    web3 = new Web3(window.ethereum);
    try {
      await window.ethereum.enable();
      const accounts = await web3.eth.getAccounts();
      account = accounts[0];
      console.log("Connected to MetaMask:", account);

      initializeQuiz();
    } catch (error) {
      console.error("Error connecting to MetaMask:", error);
    }
  } else {
    console.error(
      "MetaMask not found. Please install MetaMask to play the game."
    );
  }
}
/*=======================================================================================================================================*/

async function initializeQuiz() {
  quizData = [
    {
      question: "What is the capital of France?",
      options: ["Paris", "Berlin", "Rome", "Madrid"],
      correctIndex: 0,
    },
    {
      question: "Which planet is closest to the Sun?",
      options: ["Mercury", "Venus", "Earth", "Mars"],
      correctIndex: 0,
    },
    {
      question: "Which color of the rainbow do unicorns come in?",
      options: ["Red", "Orange", "Yellow", "Blue"],
      correctIndex: 1,
    },
  ];

  document.getElementById("connectWalletBtn").style.display = "none";
  document.getElementById("quiz").style.display = "block";
  loadCurrentQuestion();
}
/*=======================================================================================================================================*/

function loadCurrentQuestion() {
  if (currentQuestionIndex < quizData.length) {
    document.getElementById("questionTitle").innerText =
      quizData[currentQuestionIndex].question;
    let optionsHTML = "";
    quizData[currentQuestionIndex].options.forEach((option, index) => {
      optionsHTML += `<button onclick="selectOption(${currentQuestionIndex}, ${index})">${option}</button>`;
    });
    document.getElementById("options").innerHTML = optionsHTML;
  } else {
    redeemReward();
  }
}
/*=======================================================================================================================================*/

function selectOption(questionIndex, selectedIndex) {
  const selectedOption = quizData[questionIndex].options[selectedIndex];
  const correctOption =
    quizData[questionIndex].options[quizData[questionIndex].correctIndex];

  if (selectedOption === correctOption) {
    document.getElementById("result").innerText = "Correct!";
    currentQuestionIndex++;
    loadCurrentQuestion();
  } else {
    document.getElementById("result").innerText =
      "Incorrect! Please try again.";
  }
}
/*=======================================================================================================================================*/
async function redeemReward() {
  if (!redeemedReward) {
    const rewards = quizData.length * 0.1;

    try {
      const tokenContract = new web3.eth.Contract(
        tokenABI,
        tokenContractAddress
      );
      const rewardsInSmallestUnit = web3.utils.toWei(
        rewards.toString(),
        "ether"
      );
      await tokenContract.methods
        .RewardUser(account, rewardsInSmallestUnit)
        .send({ from: account });
      console.log("Yayy");
    } catch (error) {
      console.error("Error transferring ASH:", error);
      document.getElementById("result").innerText = `Error: ${
        error.message || error
      }`;
      return;
    }
    redeemedReward = true;
    document.getElementById("result").innerText = "Rewards Redeemed!";
    document.getElementById("redeemRewardBtn").style.display = "none";
  }
}
document
  .getElementById("connectWalletBtn")
  .addEventListener("click", connectWallet);
document
  .getElementById("redeemRewardBtn")
  .addEventListener("click", redeemReward);
/*=======================================================================================================================================*/
function openDialog(content) {
  const dialogBox = document.getElementById('info-dialog');
  const dialogContent = document.getElementById('dialog-content');

  dialogContent.innerHTML = content;
  dialogBox.style.display = 'flex';
}

function closeDialog() {
  const dialogBox = document.getElementById('info-dialog');
  dialogBox.style.display = 'none';
}
